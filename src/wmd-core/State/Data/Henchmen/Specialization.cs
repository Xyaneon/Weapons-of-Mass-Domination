namespace WMD.Game.State.Data.Henchmen;

/// <summary>
/// Indicates the specialization of a player's henchmen.
/// </summary>
public enum Specialization
{
    /// <summary>
    /// Indicates that the player's henchmen are untrained (i.e., have no
    /// specialization yet).
    /// </summary>
    Untrained,
    /// <summary>
    /// Indicates that the player's henchmen are soldiers.
    /// </summary>
    Soldier,
}
